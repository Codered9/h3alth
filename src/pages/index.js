import Head from "next/head";
import "primereact/resources/themes/lara-light-indigo/theme.css";
import "primereact/resources/primereact.min.css";
import "primeicons/primeicons.css";
import { useEffect } from "react";
import getAuth from "../auth/getArcanaAuth";
import { Button } from "primereact/button";
import { Panel } from 'primereact/panel';
import { useAuth } from "@arcana/auth-react";
import UploadFile from "@/components/UploadFile";
import NavBar from "@/components/NavBar";
export default function Index() {
  const { user, connect, isLoggedIn, loading, loginWithSocial, provider } =
    useAuth();
  const auth = useAuth();

  const onConnectClick = async () => {
    try {
      await connect();
    } catch (e) {
      console.log("Chain not added. Error");
    }
  };

  const addHyperSpace = async () => {
    try{
      await provider.request({
        method: 'wallet_addEthereumChain',
        params: [{
          chainId: '0xC45',
          chainName: 'Filecoin - Hyperspace testnet',
          rpcUrls: ['https://filecoin-hyperspace.chainstacklabs.com/rpc/v1', 'https://api.hyperspace.node.glif.io/rpc/v1'],
          blockExplorerUrls: ['https://hyperspace.filfox.info/en', ],
          nativeCurrency: {
            name: 'USD',
            symbol: '$', // 2-6 characters long
            decimals: 18
          }
        }]
      })
    } catch(e){
      console.log(e)
    }
  }

  const onConnect = async () => {
    console.log("connected");
    addHyperSpace();
  };
  useEffect(() => {
    provider.on("connect", onConnect);
    return () => {
      provider.removeListener("connect", onConnect);
    };
  }, [provider]);
  return (
    <div style={{padding: "2rem", backgroundColor: "white"}}>
      <Head>
        <title>H3alth</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <NavBar />
        <p className="m-0" style={{margin: "2rem", fontSize: "18px", fontFamily: '-apple-system, BlinkMacSystemFont, "Segoe UI", Roboto, Helvetica, Arial, sans-serif, "Apple Color Emoji", "Segoe UI Emoji", "Segoe UI Symbol"', lineHeight: "22px", backgroundColor: "white"}}>
          Welcome to H3alth Home Page. Here you can upload your medical reports to decentralised storage. <br/>
          Below this panel you can see file upload menu using which you can upload files. <br/>
          At the bottom right you can see your Arcana Wallet which contains all your assets, NFTs, past acitivity and Logout button to exit the session.
        </p>
      {isLoggedIn ? (
        ""
      ) : (
        <Button label="Connect" raised onClick={onConnectClick} />
      )}
      {isLoggedIn ? user && <UploadFile auth={auth} /> : ""}
    </div>
  );
}
